package com.restaurant.dto;

public class Chef {
  private Integer chef_id;
  private String chef_name;
  private String ch_email;
  private Long phone_no;
  private String ch_pswd;
  public Integer getChef_id() {
	return chef_id;
  }
  public void setChef_id(Integer chef_id) {
	this.chef_id = chef_id;
  }
  public String getChef_name() {
	return chef_name;
  }
  public void setChef_name(String chef_name) {
	this.chef_name = chef_name;
  }
  public String getCh_email() {
	return ch_email;
  }
  public void setCh_email(String ch_email) {
	this.ch_email = ch_email;
  }
  public Long getPhone_no() {
	return phone_no;
  }
  public void setPhone_no(Long phone_no) {
	this.phone_no = phone_no;
  }
  public String getCh_pswd() {
	return ch_pswd;
  }
  public void setCh_pswd(String ch_pswd) {
	this.ch_pswd = ch_pswd;
  }
  
  
}
